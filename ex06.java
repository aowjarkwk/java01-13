package examples;

public class ex06 {

	public static void main(String[] args) {
		// 연습문제 - DM제출
		//정수형 변수 a, b를 선언하고,
		//각각 10, 3 값을 a, b에 대입(=)하고,
		// a값과 b값을 덧셈한 결과(+)를 출력하고,
		// a값과 b값을 뺄셈한 결과(-)를 출력하고,
		// a값과 b값을 곱셈한 결과(*)를 출력하고,
		// a값과 b값을 나눗셈의 몫(/)을 출력하시오.
		// a값과 b값을 나눗셈의 나머지(%)를 출력하시오.
		
		int a=10,b=3;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);

	}

}
